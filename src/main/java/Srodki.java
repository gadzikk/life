public class Srodki {
    boolean ludzieTwejStrony;
    boolean przekonywanie;

    boolean piesci;
    boolean sprzet;
    boolean klamstwo;
    boolean obelgi;

    boolean standardy;
    boolean prawo;

    public Srodki(boolean ludzieTwejStrony, boolean przekonywanie, boolean piesci, boolean sprzet, boolean klamstwo, boolean obelgi, boolean standardy, boolean prawo) {
        this.ludzieTwejStrony = ludzieTwejStrony;
        this.przekonywanie = przekonywanie;
        this.piesci = piesci;
        this.sprzet = sprzet;
        this.klamstwo = klamstwo;
        this.obelgi = obelgi;
        this.standardy = standardy;
        this.prawo = prawo;
    }
}
